############# print vs puts vs p ###############
## print: no new line at the end
## puts: new line at the end
## p: print the argument as it is and returns the argument
print 'Good morning.'
puts 'Hello World!'
puts 'Come on.'
p 'Hello World!'
p ['one', 'two', 3]

## output:
# Good morning.Hello World!
# Come on.
# "Hello World!"
# ["one", "two", 3]


